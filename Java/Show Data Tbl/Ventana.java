    private void cargarTabla() {
        model.addColumn("Id");
        model.addColumn("Codigo");
        model.addColumn("Nombre");
        model.addColumn("Precio");
        model.addColumn("Cantidad");
        model.addColumn("Categoria");

        consultarDatos();
        tblDatos.setModel(model);
    }

    private void consultarDatos() {
        String query = "SELECT * FROM producto";
        try {
            ps = con.prepareStatement(query);
            rs = ps.executeQuery();

            while (rs.next()) {
                Object fila[] = new Object[model.getColumnCount()];
                for (int i = 0; i < fila.length; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                model.addRow(fila);
            }
        } catch (SQLException e) {
            System.err.println("Error: " + e.getMessage());
        }
    }

    private void buscarDatos() {
        String query = "SELECT * FROM producto WHERE codigo=?";
        try {
            ps = con.prepareStatement(query);
            ps.setString(1, txtCodigoBusqueda.getText());
            rs = ps.executeQuery();

            while (rs.next()) {
                Object fila[] = new Object[model.getColumnCount()];
                for (int i = 0; i < fila.length; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                limpiarTabla();
                model.addRow(fila);
            }
        } catch (SQLException e) {
            System.err.println("Error: " + e.getMessage());
        }
    }

    private void limpiarTabla() {
        for (int i = 0; i < tblDatos.getRowCount(); i++) {
            model.removeRow(i);
            i -= 1;
        }
    }

    private void guardarBD() {
        String sql = "INSERT INTO producto "
                + "(codigo, nombre, precio, cantidad, categoria)"
                + "VALUES "
                + "(?,?,?,?,?)";
        try {
            ps = con.prepareStatement(sql);
            ps.setString(1, txtCodigo.getText());
            ps.setString(2, txtNombre.getText());
            ps.setDouble(3, Double.parseDouble(txtPrecio.getText()));
            ps.setInt(4, Integer.parseInt(txtCantidad.getText()));
            ps.setString(5, (cmbCategoria.getSelectedItem().toString()));

            int result = ps.executeUpdate();

            if (result != 0) {
                JOptionPane.showMessageDialog(null, "Dato Guardado");
                clearForm();
                refrescarTabla();
            } else {
                JOptionPane.showMessageDialog(null, "Error al Guardar");
            }

        } catch (HeadlessException | NumberFormatException | SQLException e) {
            System.err.println("Error: " + e.getMessage());
        }
    }

    private void actualizarBD() {
        String sql = "UPDATE producto SET "
                + "codigo=?, nombre=?, precio=?, cantidad=? "
                + "WHERE idProducto=?";
        try {
            ps = con.prepareStatement(sql);
            ps.setString(1, txtCodigo.getText());
            ps.setString(2, txtNombre.getText());
            ps.setDouble(3, Double.parseDouble(txtPrecio.getText()));
            ps.setInt(4, Integer.parseInt(txtCantidad.getText()));
            ps.setInt(5, Integer.parseInt(txtId.getText()));

            int result = ps.executeUpdate();

            if (result != 0) {
                JOptionPane.showMessageDialog(null, "Dato actualizado");
                clearForm();
                refrescarTabla();
            } else {
                JOptionPane.showMessageDialog(null, "Error al Actualizar");
            }

        } catch (HeadlessException | NumberFormatException | SQLException e) {
            System.err.println("Error: " + e.getMessage());
        }
    }

    private void eliminarBD() {
        String sql = "DELETE FROM producto "
                + "WHERE idProducto=?";
        try {
            ps = con.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(txtId.getText()));

            int result = ps.executeUpdate();

            if (result != 0) {
                JOptionPane.showMessageDialog(null, "Dato eliminado");
                clearForm();
                refrescarTabla();
            } else {
                JOptionPane.showMessageDialog(null, "Error al Eliminar");
            }

        } catch (HeadlessException | NumberFormatException | SQLException e) {
            System.err.println("Error: " + e.getMessage());
        }
    }

    private void refrescarTabla() {
        model.setColumnCount(0);
        model.setRowCount(0);
        cargarTabla();
    }

    private void cargarCmb() {
        cmbCategoria.addItem("--Selecciona--");
        String sql = "SELECT * FROM categoria";

        try {
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            
            while(rs.next()){
                cmbCategoria.addItem(rs.getString("nombre"));
            }
        } catch (SQLException e) {
            System.out.println("Error: "+e.getMessage());
        }
    }

    private void clearForm() {
        txtNombre.setText("");
        txtCantidad.setText("");
        txtPrecio.setText("");
        txtCodigo.setText("");
        txtId.setText("");
        txtCodigoBusqueda.setText("");
        cmbCategoria.setSelectedItem("--Selecciona--");
    }
